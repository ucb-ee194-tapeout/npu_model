bf16 e4m3_mac(e4m3 *activations, e4m3 *weights, size_t N, e4m3 bias)
{
	// convert numbers
	fp16 *activations_fp16 = malloc(sizeof(fp16) * N);
	fp16 *weights_fp16 = malloc(sizeof(fp16) * N);

	for (int i = 0; i < N; i++)
	{
		activations_fp16[i] = e4m3_to_fp16(activations[i]);
		weights_fp16[i] = e4m3_to_fp16(weights[i]);
	}

	fp16 *products = malloc(sizeof(fp16) * N);
	// perform element wise multiplication
	for (int i = 0; i < N; i++)
	{
		products[i] = mul_fp16(activations_fp16[i], weights_fp16[i]);
	}

	uint8_t anchor = generate_anchor_fp16(products, N, bias);

	uint32_t *fixed_point_products = malloc(sizeof(uint32_t) * N);

	for (int i = 0; i < N; i++)
	{
		fixed_point_products[i] = fp16_to_int_align(products[i], anchor);
	}

	uint32_t fixed_point_bias = e4m3_to_int_align(bias, anchor);

	uint32_t reduction = fixed_point_int_reduction(fixed_point_products, N, fixed_point_bias);

	bf16 result = int_to_bf16(reduction, anchor);


	return result;
}

bool run_mac_test(
	const uint8_t *act_hex,
	const uint8_t *weight_hex,
	size_t N,
	uint8_t bias_hex,
	uint16_t expected_result)
{
	e4m3 *activations = malloc(sizeof(e4m3) * N);
	e4m3 *weights = malloc(sizeof(e4m3) * N);

	// Populate vectors from hex inputs
	for (size_t i = 0; i < N; i++)
	{
		activations[i].byte = act_hex[i];
		weights[i].byte = weight_hex[i];
	}

	e4m3 bias;
	bias.byte = bias_hex;

	bf16 result = e4m3_mac(activations, weights, N, bias);

	bool passed = (result.half == expected_result);
	if (!passed)
		printf("Expected: 0x%04x, Got: 0x%04x\n", expected_result, result);

	free(activations);
	free(weights);

	return passed;
}